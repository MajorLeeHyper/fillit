void	ft_ofs_len(int *ofs, int *len, char *tab)
{
	*ofs = 0;
	*len = 0;

	while (tab[*len] != '\n')
		*len = *len + 1;
	*len = *len + 1;
	while (tab[*ofs] != '.')
		*ofs = *ofs + 1;
}
